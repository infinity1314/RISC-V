module ALU(
            input wire [31:0]  source1,
            input wire [31:0]  source2,
            input wire [4:0]   ALUCtrl,
            output reg [0:0]   Zero   ,
            output reg [31:0]  result
        );   

always @(*)
  begin
    case (ALUCtrl)
//////////////////////////////////////////////////////////////////////////////////////////////////	
//////////////////////////////////////加法////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////	
		5'b00100 : begin 
		               result = source1 + source2;
							Zero = 0;
				   end
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////有符号减法1////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b00101 : begin //                      
		               if(source1[31] == 0 && source2[31] == 0) begin//都为正数
								if(source1[30:0] > source2[30:0]) begin
										 result[31] = 0;
                               result[30:0] = source1[30:0] - source2[30:0];
										 Zero = 0;
							   end
								else if(source1[30:0] == source2[30:0]) begin	
									 result = 31'b0;
									 Zero = 1;
									 end
								else
								//if(source1[30:0] < source2[30:0]) 
								begin
                               result[31] = 1;	
										 result[30:0] = ~(source2[30:0] - source1[30:0]) + 1;
                               Zero = 0;
                               end
                            end
                       else
                       if(source1[31] == 1 && source2[31] == 1) begin//都为负数
                           if(source1[30:0] > source2[30:0]) begin
										 result[31] = 1;
                               result[30:0] = ~(source1[30:0] - source2[30:0]) + 1;
										 Zero = 0;
									end
                           else
                           if(source1[30:0] == source2[30:0]) begin	
                               result = 31'b0;
                               Zero = 1;
                               end
                           else
									//if(source1[30:0] < source2[30:0]) 
									begin
                               result[31] = 0;	
										 result[30:0] = source2[30:0] - source1[30:0];
                               Zero = 0;
                               end
                            end
                       else if(source1[31] < source2[31]) begin//正数-负数
									result[31] = 0;
									result[30:0] = source1[30:0] + source2[30:0];
									Zero = 0;
								end
                       else //if(source1[31] > source2[31]) 
							  begin//负数-正数
									result[31] = 1;
									result[30:0] = ~(source1[30:0] + source2[30:0]) + 1;
									Zero = 0;
							  end
                   end	
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////判断是否相等////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b00111 : begin
		               if(source1 == source2) begin
                           result = 0;
                           Zero = 0;
                           end
                       else begin
                           result = 1;
                           Zero = 1;
                           end						   
		           end
//////////////////////////////////////////////////////////////////////////////////////////////////	
//////////////////////////////////////////与运算//////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////				   
		5'b11100 : begin
		               result = source1 & source2;
							Zero = 0;
				   end
//////////////////////////////////////////////////////////////////////////////////////////////////	
//////////////////////////////////////////或运算//////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b11101 : begin
		              result = source1 | source2;
						  Zero = 0;
				   end
//////////////////////////////////////////////////////////////////////////////////////////////////	
//////////////////////////////////////////异或运算////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b11110 : begin
		               result = source1 ^ source2;
							Zero = 0;
				   end	   
//////////////////////////////////////////////////////////////////////////////////////////////////	
//////////////////////////////////////////无符号小于</////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////		
		5'b10000 : begin                      
		               if(source1 < source2) begin
								Zero = 1;
								result = 1;
						   end
					   else begin
					       Zero = 0;
						   result = 0;
						   end
				   end  
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////有符号小于<///////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////				   
		5'b10100 : begin                            
				       if(source1[31]>source2[31]) begin
					       Zero = 1;
								result = 1;
						   end
					   else if(source1[31] ==0 && source2[31] == 0)
						   begin
							   if(source1 < source2) begin
								   Zero = 1;
								   result = 1;
								   end
							   else begin
								   Zero = 0;
								   result = 0;
								   end
						   end
					   else if(source1[31] == 1 && source2[31] == 1)
						   begin
							   if(source1 > source2) begin
									Zero = 1;
									result = 1;
									end
							   else begin
									Zero = 0;
									result = 0;
									end
						   end
					   else begin
							Zero = 0;
							result = 0;
							end
					end
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////无符号大于等于>=//////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b10001 :  begin                       
                        if(source1 > source2) begin
                            Zero = 1;
									 result = 1;	
                            end
                        else
                        if(source1 == source2) begin
                            Zero = 1;
									 result = 1;	
                            end
                        else begin
                            Zero = 0;
									 result = 0;	
                            end
                    end	
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////有符号大于等于>=//////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////					
		5'b10101 :  begin                       
						if(source1[31] < source2[31]) begin
							Zero = 1;
							result = 1;	
							end
						else if(source1[31] ==0 && source2[31] == 0) begin
							if(source1 < source2) begin
								Zero = 0;
								result = 0;
								end
							else begin
								Zero = 1;
								result = 1;
								end
							end
						else if(source1[31] == 1 && source2[31] == 1) begin
							if(source1 > source2) begin
								Zero = 0;
								result = 0;
								end
							else begin
								Zero = 1;
								result = 1;
								end
							end
						else begin
							Zero = 0;
							result = 0;
							end
					end 
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////逻辑右移//////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b01101 :  begin
		            result = source1 >> source2[4:0];   
						if(result == 0)
						    Zero = 1;
						else
						    Zero = 0;
					end
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////逻辑左移//////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
		5'b01100 :  begin
		                result = source1 << source2[4:0]; 
							 if(result == 0)
								Zero = 1;
							 else
								Zero = 0;
					end
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////算数右移//////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////
        5'b01110 :  begin
		                result = source1 >> source2[4:0]; begin
						    if(source1[31] == 1) begin
							    case(source2[4:0]) 
								    5'b00000 : result[31]    = 1'b1;
									5'b00001 : result[31]    = 1'b1;
									5'b00010 : result[31:30] = 2'b11;
									5'b00011 : result[31:29] = 3'b111;
									5'b00100 : result[31:28] = 4'b1111;
									5'b00101 : result[31:27] = 5'b1111_1;
									5'b00110 : result[31:26] = 6'b1111_11;
									5'b00111 : result[31:25] = 7'b1111_111;
									5'b01000 : result[31:24] = 8'b1111_1111;
									5'b01001 : result[31:23] = 9'b1111_1111_1;
									5'b01010 : result[31:22] = 10'b1111_1111_11;
									5'b01011 : result[31:21] = 11'b1111_1111_111;
									5'b01100 : result[31:20] = 12'b1111_1111_1111;
									5'b01101 : result[31:19] = 13'b1111_1111_1111_1;
									5'b01110 : result[31:18] = 14'b1111_1111_1111_11;
									5'b01111 : result[31:17] = 15'b1111_1111_1111_111;
									5'b10000 : result[31:16] = 16'b1111_1111_1111_1111;
									5'b10001 : result[31:15] = 17'b1111_1111_1111_1111_1;
									5'b10010 : result[31:14] = 18'b1111_1111_1111_1111_11;
									5'b10011 : result[31:13] = 19'b1111_1111_1111_1111_111;
									5'b10100 : result[31:12] = 20'b1111_1111_1111_1111_1111;
									5'b10101 : result[31:11] = 21'b1111_1111_1111_1111_1111_1;
									5'b10110 : result[31:10] = 22'b1111_1111_1111_1111_1111_11;
									5'b10111 : result[31:9 ] = 23'b1111_1111_1111_1111_1111_111;
									5'b11000 : result[31:8 ] = 24'b1111_1111_1111_1111_1111_1111;
									5'b11001 : result[31:7 ] = 25'b1111_1111_1111_1111_1111_1111_1;
									5'b11010 : result[31:6 ] = 26'b1111_1111_1111_1111_1111_1111_11;
									5'b11011 : result[31:5 ] = 27'b1111_1111_1111_1111_1111_1111_111;
									5'b11100 : result[31:4 ] = 28'b1111_1111_1111_1111_1111_1111_1111;
									5'b11101 : result[31:3 ] = 29'b1111_1111_1111_1111_1111_1111_1111_1;
									5'b11110 : result[31:2 ] = 30'b1111_1111_1111_1111_1111_1111_1111_11;
									5'b11111 : result[31:1 ] = 31'b1111_1111_1111_1111_1111_1111_1111_111;
									default  : result[31]    = 1'b1;
								endcase
								end
							else
                            if(source1[31] == 0) begin
                                case(source2[4:0])
								    5'b00000 : result[31]    = 'b0;
									5'b00001 : result[31]    = 'b0;
									5'b00010 : result[31:30] = 'b0;
									5'b00011 : result[31:29] = 'b0;
									5'b00100 : result[31:28] = 'b0;
									5'b00101 : result[31:27] = 'b0;
									5'b00110 : result[31:26] = 'b0;
									5'b00111 : result[31:25] = 'b0;
									5'b01000 : result[31:24] = 'b0;
									5'b01001 : result[31:23] = 'b0;
									5'b01010 : result[31:22] = 'b0;
									5'b01011 : result[31:21] = 'b0;
									5'b01100 : result[31:20] = 'b0;
									5'b01101 : result[31:19] = 'b0;
									5'b01110 : result[31:18] = 'b0;
									5'b01111 : result[31:17] = 'b0;
									5'b10000 : result[31:16] = 'b0;
									5'b10001 : result[31:15] = 'b0;
									5'b10010 : result[31:14] = 'b0;
									5'b10011 : result[31:13] = 'b0;
									5'b10100 : result[31:12] = 'b0;
									5'b10101 : result[31:11] = 'b0;
									5'b10110 : result[31:10] = 'b0;
									5'b10111 : result[31:9 ] = 'b0;
									5'b11000 : result[31:8 ] = 'b0;
									5'b11001 : result[31:7 ] = 'b0;
									5'b11010 : result[31:6 ] = 'b0;
									5'b11011 : result[31:5 ] = 'b0;
									5'b11100 : result[31:4 ] = 'b0;
									5'b11101 : result[31:3 ] = 'b0;
									5'b11110 : result[31:2 ] = 'b0;
									5'b11111 : result[31:1 ] = 'b0;
									default  : result[31]    = 'b0;
								endcase
				                end
                            else
                                result = 0;
							end
                    end    								
//////////////////////////////////////////////////////////////////////////////////////////////////	
////////////////////////////////////////////jal强制输出0//////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////	
        5'b00110 :  begin
		                Zero = 1;
                        result = 1;
                    end						
        default  :  begin
		                Zero = 0;
                        result = 0;
                    end	
	endcase
 end
 
endmodule
	
	
	
	